    private void txtPretragaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretragaKeyReleased
        String parametar = txtPretraga.getText();
        TableModelKorisnik tmKorisnik = (TableModelKorisnik) tblKorisnik.getModel();
        tmKorisnik.setParametar(parametar);
        if (tmKorisnik.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Sistem ne moze da nadje korisnike po zadatoj vrednosti",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_txtPretragaKeyReleased

    private void btnIzmeniKorisnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniKorisnikaActionPerformed
        int rowIndex=tblKorisnik.getSelectedRow();
        if(rowIndex>=0){
            TableModelKorisnik tm=(TableModelKorisnik) tblKorisnik.getModel();
            Korisnik korisnik= tm.getKorisnik(rowIndex);
            JOptionPane.showMessageDialog(this,"Sistem je ucitao korisnika.");
            new FrmUpdateKorisnik(this, true,korisnik).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this,"Sistem ne moze da ucita korisnika","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniKorisnikaActionPerformed

    private void btnObrisiKorisnikaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiKorisnikaActionPerformed
      
        TableModelKorisnik tmKorisnik = (TableModelKorisnik) tblKorisnik.getModel();
        int rowIndex = tblKorisnik.getSelectedRow();
        if (rowIndex >= 0) {
            int deleteYesNo = JOptionPane.showConfirmDialog(this,
                    "Da li ste sigurni da zelite da obrisete ovog korisnika?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

            if (deleteYesNo == JOptionPane.NO_OPTION) {
                return;
            }

            if (deleteYesNo == JOptionPane.YES_OPTION) {
                try {
                    Korisnik korisnik = tmKorisnik.getKorisnik(rowIndex);
                    ClientController.getInstance().deleteKorisnik(korisnik);
                    refreshTableKorisnik();
                    JOptionPane.showMessageDialog(this, "Sistem je obrisao korisnika.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Sistem ne moze da obrise korisnika", "Error", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da obrise korisnika", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnObrisiKorisnikaActionPerformed

